<?php
// Criando as variáveis
$a = 55.0;
$b = 55;
// Operadores lógicos
// Maior que
var_dump($a > $b);
// Maior igual que
var_dump($a >= $b);
// Menor igual que
var_dump($a <= $b);
// Igual
// Operador de igualdade de valor
var_dump($a == $b);
// Operador de igualdade de identidade
// Igual e de mesmos tipos
var_dump($a === $b);
// Diferente de validando somente valor
var_dump($a != $b);
// Diferente de validando valor e tipo
var_dump($a !== $b);

 ?>
